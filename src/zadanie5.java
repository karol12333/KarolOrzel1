        int po=3*4;
        int pom=2*3;
        int pod=2*2;
        System.out.println("Pierwsze pomieszczenie ma wymiary: " + p +"m^2");
        System.out.println("Drugie pomieszczenie ma wymiary: " + po +"m^2" );
